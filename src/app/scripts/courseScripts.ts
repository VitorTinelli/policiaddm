/**
 * Obtém o script de um curso específico
 * @param courseSigla - Sigla do curso (ex: 'CFI', 'CFC')
 * @returns Array de strings com as frases do script, ou array vazio se não encontrado
 */
export const getCourseScript = (courseSigla: string): string[] => {
  return courseScripts[courseSigla] || [];
};

/**
 * Verifica se um curso tem script específico disponível
 * @param courseSigla - Sigla do curso
 * @returns true se o curso tem script, false caso contrário
 */
export const hasCourseScript = (courseSigla: string): boolean => {
  return courseSigla in courseScripts;
};

/**
 * Obtém lista de todos os cursos que têm scripts disponíveis
 * @returns Array com as siglas dos cursos que têm scripts
 */
export const getAvailableCourseScripts = (): string[] => {
  return Object.keys(courseScripts);
};
